template <typename T>
ostream& operator<<(ostream& os, const vector<T>& v)
{
    os << "[";
    for (int i = 0; i < v.size(); ++i) {
        os << v[i];
        if (i != v.size() - 1)
            os << ", ";
    }
    os << "]\n";
    return os;
}

int find_occurence(const vector<int> &v, int key, int mid) {
    int left = mid-1;
    int right = mid+1;
    while(v[left] == key) {
        left--;
    }

    while(v[right] == key) {
        right++;
    }
    return right-left-1;
}

int findCount(const vector<int> &v, int key) {
    int low = 0;
    int high = v.size() -1;
    while(low <= high) {
        int mid = (low+high)/2;
        if(v[mid] == key) {
            return find_occurence(v, key, mid);
        }
        else if (key > v[mid]) {
            low = mid + 1;
        } else {
            high = mid - 1;
        }
    }
    return 0;

}


